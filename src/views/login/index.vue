<template>
  <div class="login-container">
    <el-form ref="loginForm" :model="loginForm" :rules="loginRules" class="login-form" auto-complete="on" label-position="left">

      <div class="title-container">
        <h3 class="title">登 录</h3>
      </div>

      <el-form-item prop="工号">
        <span class="svg-container">
          <svg-icon icon-class="user" />
        </span>
        <el-input
          ref="jobID"
          v-model="loginForm.jobID"
          placeholder="请输入工号"
          name="jobID"
          type="text"
          tabindex="1"
          auto-complete="on"
        />
      </el-form-item>

      <el-form-item prop="password">
        <span class="svg-container">
          <svg-icon icon-class="password" />
        </span>
        <el-input
          :key="passwordType"
          ref="password"
          v-model="loginForm.password"
          :type="passwordType"
          placeholder="Password"
          name="password"
          tabindex="2"
          auto-complete="on"
          @keyup.enter.native="handleLogin"
        />
        <span class="show-pwd" @click="showPwd">
          <svg-icon :icon-class="passwordType === 'password' ? 'eye' : 'eye-open'" />
        </span>
      </el-form-item>

      <el-button :loading="loading" type="primary" style="width:100%;margin-bottom:30px;" @click.native.prevent="handleLogin">Login</el-button>

    </el-form>
  </div>
</template>

<script>
// import { validUsername } from '@/utils/validate'---验证用户名的地方，如果加入正则表达式验证，则在此文件加入
import { login } from '@/api/user'
export default {
  name: 'Login',
  data() {
    const validateUsername = (rule, value, callback) => {
      // if (!validUsername(value)) {
      if (value.length < 9) {
        callback(new Error('Please enter the correct user name'))
      } else {
        callback()
      }
    }
    const validatePassword = (rule, value, callback) => {
      if (value.length < 6) {
        callback(new Error('The password can not be less than 6 digits'))
      } else {
        callback()
      }
    }
    return {
      loginForm: {
        jobID: '',
        password: ''
      },
      loginRules: {
        jobID: [{ required: true, trigger: 'blur', validator: validateUsername }],
        password: [{ required: true, trigger: 'blur', validator: validatePassword }]
      },
      loading: false,
      passwordType: 'password',
      // redirect: undefined
    }
  },
  // watch: {
  //   $route: {
  //     handler: function(route) {
  //       this.redirect = route.query && route.query.redirect;
  //     },
  //     immediate: true
  //   }
  // },
  methods: {
    showPwd() {
      if (this.passwordType === 'password') {
        this.passwordType = ''
      } else {
        this.passwordType = 'password'
      }
      this.$nextTick(() => {
        this.$refs.password.focus()
      })
    },
    handleLogin() {
      const t = this;
      t.$refs.loginForm.validate(valid => {
        if (valid) {
          const t = this;
          t.$store.dispatch('user/login', t.loginForm).then((res) => {
            if (res.code === 200) {
              t.$message({
                type:'success',
                message:res.message
              })
              if (!res.result.station && !res.result.department) {
                this.$router.push({ path:'./user'})
                this.$message({
                  type:'warning',
                  message:'请先完善个人信息！'
                })
              } else {
                // this.$router.push({ path: this.redirect || '/' })
                this.$router.push({ path:'/'})
                t.$store.dispatch('user/getInfo').then(res => {
                  console.log('res 获取用户个人信息:>> ', res);
                })
                
              }
            }
            this.loading = false
          }).catch(() => {
            this.loading = false
          })
        } else {
          return false
        }
      })
    }
  }
}
</script>

